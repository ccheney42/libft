#include "libft.h"

int	ft_atoi(const char *str)
{
	int x;
	int y;
	int z;

	x = 0;
	y = 0;
	z = 1;
	while (str[x] == ' ' || str[x] == '\n' || str[x] == '\v' ||
			str[x] == '\t' || str[x] == '\b' || str[x] == '\f'
			|| str[x] == '\r')
		x++;
	if (str[x] == '+' || str[x] == '-')
	{
		if (str[x] == '-')
			z = -1;
		x++;
	}
	while (str[x] != '\0' && (str[x] >= '0' && str[x] <= '9'))
	{
		y = y * 10 + str[x] - '0';
		x++;
	}
	y *= z;
	return (y);
}
